/*!
    \brief      this function handles SysTick exception
    \param[in]  none
    \param[out] none
    \retval     none
*/
void SysTick_Handler(void)
{
	if(tos_knl_is_running())
	{
		tos_knl_irq_enter();
		tos_tick_handler();          
		tos_knl_irq_leave();
	}
    delay_decrement();
}
